package cn.herodotus.eurynome.assistant.exception.platform;

/**
 * <p>Description: 持久化错误 </p>
 *
 * @author : gengwei.zheng
 * @date : 2021/9/23 15:12
 */
public class PersistenceException extends PlatformException {

    public PersistenceException() {
        super();
    }

    public PersistenceException(String message) {
        super(message);
    }

    public PersistenceException(String message, Throwable cause) {
        super(message, cause);
    }

    public PersistenceException(Throwable cause) {
        super(cause);
    }

    public PersistenceException(String message, Throwable cause, boolean enableSuppression, boolean writableStackTrace) {
        super(message, cause, enableSuppression, writableStackTrace);
    }
}
